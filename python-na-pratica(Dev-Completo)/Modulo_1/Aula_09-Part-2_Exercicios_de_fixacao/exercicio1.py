a = 10
b = 3

soma = (a + b)
subtracao = (a - b)
multiplicacao = (a * b)
divisao = (a / b)
divisao_inteira = (a // b)
expoente = (a ** b)
resto_divisao = (a % b)

print(f"Resultado da soma: {soma}")
print(f"Resultado da subtração: {subtracao}")
print(f"Resultado da multiplicação: {multiplicacao}")
print(f"Resultado da divisão: {divisao}")
print(f"Resultado da divisão inteira: {divisao_inteira}")
print(f"Resultado da exponenciação: {expoente}")
print(f"Resultado do resto da divisão: {resto_divisao}")

