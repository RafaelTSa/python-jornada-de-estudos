a = 10
b = 11

print(a == b)  # compara se a é "IGUAL" ao b com operador de comparação ( == )
print(a != b)  # compara se a é "DIFERENTE" de b com operador de comparação ( != )
print(a > b)   # compara se a é "MAIOR" ao b com operador de comparação ( > )
print(a < b)   # compara se a é "MENOR" ao b com operador de comparação ( < )
print(a >= b)  # compara se a é "MAIOR OU IGUAL" ao b com operador de comparação ( >= )
print(a <= b) # compara se a é "MENOR OU IGUAL" ao b com operador de comparação ( <= )
