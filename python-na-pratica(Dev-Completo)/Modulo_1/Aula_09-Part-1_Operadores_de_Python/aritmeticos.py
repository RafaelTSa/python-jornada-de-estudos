x = 10
y = 3

print(x + y)  # mostra a soma " + " entre as variáveis.
print(x - y)  # mostra a subtração " - " entre as variáveis.
print(x * y)  # mostra a multiplicação " * "entre as variáveis.
print(x / y)  # mostra a divisão " / " entre as variáveis.

print(x // y)  # mostra a divisão inteira " // " entre as variáveis (sem mostrar a parte decimal).

print(x ** y) # mostra a exponenciação " Xº "entre as variáveis (com o x sendo a base e o y sendo o expoente).

print(x % y) # mostra apenas o resto da divisão " % " entre as variáveis.
